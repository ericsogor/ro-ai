import pandas as pd
from sklearn.ensemble import RandomForestRegressor
from sklearn.preprocessing import StandardScaler

ss=StandardScaler()

df_train = pd.read_csv('train.csv')
df_test = pd.read_csv('test.csv')


x_train = df_train.drop(columns=['temperature'])
y_train = df_train['temperature']
x_test = df_test

x_scaled=ss.fit_transform(x_train)
x_train=pd.DataFrame(x_scaled,columns=x_train.columns)

x_scaledtest=ss.transform(x_test)
x_test=pd.DataFrame(x_scaledtest, columns=x_test.columns)

rf = RandomForestRegressor(
    n_estimators=500,
    random_state=42,
    n_jobs=-1
)

rf.fit(x_train, y_train)


y_pred = rf.predict(x_test)
print(y_pred)


submission = pd.DataFrame({
    'SampleID': df_test['SampleID'],
    'temperature': y_pred
})

submission.to_csv('submission.csv', index=False)